// Close popup menu when click outside the popup
var popup = document.getElementsByClassName('popup');

function closePopupHandle(e) {
	if (e.target.className === 'popup') {
		window.location.hash = 'services';
	}
}

for (var j=0; j < popup.length; j++) {
	popup[j].addEventListener('click', closePopupHandle);
}



// switch language functionality
var defaultLang = 'en';
var btnEn = document.getElementById('en');
var btnEs = document.getElementById('es');
var btnLang = document.getElementsByClassName('switch-lang');
var text = document.getElementsByClassName('translate');



// Click on the switch language buttons
function switchBtnClickHandle(e) {

	e.preventDefault();
	if (this.classList.contains('btn--enabled')) {
		defaultLang = (this.getAttribute('id') === 'es') ? 'es' : 'en';
		switchLang(defaultLang);
	}

}

for (var i = 0; i < btnLang.length; i++) {
  btnLang[i].addEventListener('click', switchBtnClickHandle);
}


// This function switch lang btn on the interface and update the localStorage
function switchLang(lang) {
	//Set document lang
	document.getElementsByTagName('html')[0].setAttribute('lang', lang);
	// Switch the button on UI
	btnEn.classList.toggle('btn--disabled');
	btnEn.classList.toggle('btn--enabled');
	btnEs.classList.toggle('btn--disabled');
	btnEs.classList.toggle('btn--enabled');
	// Store the language value in the local storage if it is supported
	if ('localStorage' in window) {
		localStorage.setItem('uiLang', lang);
	}
	// Start the changing the language on UI
	translation(lang);
}

